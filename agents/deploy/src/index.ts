const json = (body: unknown, status = 200) =>
  new Response(JSON.stringify(body), { status, headers: { "content-type": "application/json" } });

export default {
  async fetch(request: Request, env: Env): Promise<Response> {
    const url = new URL(request.url);

    // ── /d1/query: the secret-gated D1 proxy the containers' StoreDb calls ──
    if (url.pathname === "/d1/query" && request.method === "POST") {
      if (request.headers.get("x-d1-secret") !== env.D1_PROXY_SECRET) {
        return json({ error: "forbidden" }, 403);
      }
      let payload: { sql?: string; params?: unknown[] };
      try {
        payload = await request.json();
      } catch {
        return json({ error: "bad json body" }, 400);
      }
      if (!payload.sql) return json({ error: "missing sql" }, 400);
      try {
        const result = await env.DB.prepare(payload.sql)
          .bind(...(payload.params ?? []))
          .all();
        return json({ success: result.success, results: result.results, meta: result.meta });
      } catch (e) {
        return json({ error: String((e as Error)?.message ?? e) }, 500);
      }
    }

    // ── /worker-health: the Worker itself, no container touch ──
    if (url.pathname === "/worker-health") {
      return json({ ok: true, worker: "content-factory", at: new Date().toISOString() });
    }

    // ── hostname → container (match by subdomain prefix so both *.xpri.ai and
    // *.jackzhaojin.com route correctly through the cutover) ──
    const host = url.hostname;
    const ns =
      host.startsWith("content-factory-eval.")
        ? env.EVAL
        : host.startsWith("content-factory-gen.")
          ? env.CONTENT_GEN
          : host.startsWith("content-factory-migrate.")
            ? env.MIGRATION
            : env.COORDINATOR; // content-factory / content-factor-dash / workers.dev fallback

    // Bot gate (cost): these are public hostnames — CT-log scanners hit them
    // every few minutes (measured: 3 req/4 min at 2am), which resets sleepAfter
    // and keeps containers awake 24/7. Agent surfaces only use known prefixes;
    // answer junk from the Worker so it never wakes a container. The
    // coordinator/dash hostname can't be path-gated (Next serves everything).
    if (ns !== env.COORDINATOR) {
      const p = url.pathname;
      const legit =
        p === "/a2a" ||
        p === "/health" ||
        p.startsWith("/.well-known/") ||
        p.startsWith("/hooks/") ||
        p.startsWith("/callbacks/") ||
        p.startsWith("/artifacts/") ||
        p.startsWith("/store/");
      if (!legit) return json({ error: "not found" }, 404);
    }

    return getContainer(ns, "singleton").fetch(request);
  },
};
