      BROWSER_PERMITS: "2",
      CLAUDE_CODE_OAUTH_TOKEN: env.CLAUDE_CODE_OAUTH_TOKEN,
      // .claude.json oauthAccount stanza (generated by eval-entrypoint.sh) —
